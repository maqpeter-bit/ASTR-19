a = 1.1
b = 2.3

c = 3
d = 2

print("Sum of two floating point numbers:")
print(f"{a} + {b} = {a + b}")
print(type(a + b), "\n")

print("Difference between two integers:")
print(f"{c} - {d} = {c - d}")
print(type(c - d), "\n")

print("Product of a floating point number and an integer:")
print(f"{a} * {c} = {a * c}")
print(type(a * c), "\n")

